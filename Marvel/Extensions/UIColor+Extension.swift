import Foundation
import UIKit



extension UIColor {
    
    static var veryLightGray: UIColor {
         return UIColor(red: 236 / 255, green: 239 / 255, blue: 241 / 255, alpha: 1)
    }
    
    static var darkBlue: UIColor {
        return UIColor(red: 34 / 255, green: 32 / 255, blue: 83 / 255, alpha: 1)
    }
    
    static var almostBlack: UIColor {
        return UIColor(red: 38 / 255, green: 38 / 255, blue: 38 / 255, alpha: 1)
    }
}
